#include<iostream>
#include<vector>
#include<string>
#include<map>
using namespace std;

/*
所有 DNA 都由一系列缩写为 'A'，'C'，'G' 和 'T' 的核苷酸组成，例如："ACGAATTCCG"。在研究 DNA 时，
识别 DNA 中的重复序列有时会对研究非常有帮助。
编写一个函数来找出所有目标子串，目标子串的长度为 10，且在 DNA 字符串 s 中出现次数超过一次。
示例 1：
输入：s = "AAAAA CCCCC AAAAA CCCCC CAAAA AGGGT TT"输出：["AAAAACCCCC","CCCCCAAAAA"]
示例 2：
输入：s = "AAAAA AAAAA AAA"输出：["AAAAAAAAAA"]  AAAAA AAAAA AAA
来源：力扣（LeetCode）
链接：https://leetcode-cn.com/problems/repeated-dna-sequences
著作权归领扣网络所有。商业转载请联系官方授权，非商业转载请注明出处。
*/
class Solution {
public:
    vector<string> findRepeatedDnaSequences(string s) {
        map<string, int> count;
        vector<string> result;
        if (s.size() <= 10) {
            result.push_back(s);
            return result;
        }
        for (int i = 0; i < s.size() - 9; i++) {
            string temp;
            temp.append(s, i, 10);
            map<string, int>::iterator it = count.find(temp);
            if (it != count.end()) {
                (*it).second++;
            }
            else {
                count.insert(make_pair(temp, 1));
            }
        }
        for (map<string, int>::iterator it = count.begin(); it != count.end(); it++) {
            if ((*it).second > 1) {
                result.push_back((*it).first);
            }
        }
        return result;
    }
};

int main() {
    Solution S;
    string s1 = "AAAAACCCCCAAAAACCCCCCAAAAAGGGTTT";
    string s2 = "AAAAAAAAAAAAA";
    vector<string> result1;
    vector<string> result2;
    result1 = S.findRepeatedDnaSequences(s1);
    result2 = S.findRepeatedDnaSequences(s2);
    for (string str : result1) {
        cout << str << ",";
    }
    cout << endl;
    for (string str : result2) {
        cout << str << ",";
    }
    cout << endl;
}